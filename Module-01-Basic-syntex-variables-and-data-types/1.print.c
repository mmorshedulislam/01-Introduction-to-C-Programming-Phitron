#include <stdio.h> // header file

int main() // main program
{
    printf("Hello, world!"); // prints
    printf("HI, I am a programmer!"); // prints
    printf("Hello, world!\nHI, I am a programmer!"); // new line 
    printf("Hello, world!\tHI, I am a programmer!"); // new tab

    printf("\\"); // output "\"
    printf("100%%"); // output "100%"
    return 0; // returns
}

// special characters
// \n - new line
// \t - new tab